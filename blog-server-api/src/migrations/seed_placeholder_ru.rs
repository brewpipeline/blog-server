pub async fn exec(rb: &rbatis::RBatis) -> Result<(), Box<dyn std::error::Error>> {
    let already_done: bool = rb
        .query_decode::<u64>(
            "select count(1) as count from migration where key=?",
            vec![rbs::value!(KEY)],
        )
        .await?
        > 0;
    if already_done {
        return Ok(());
    }

    let author_count: u64 = rb
        .query_decode("select count(1) as count from author", vec![])
        .await?;

    if author_count == 0 {
        let now = time_utils::now_as_secs();
        let password_hash = password::hash(&ADMIN_PASSWORD.to_string())
            .map_err(|e| format!("password hash error: {e}"))?;

        rb.query(
            "insert into author (slug, first_name, last_name, email, password_hash, registered_at, status, image_url, editor) \
             values (?, ?, ?, ?, ?, to_timestamp(?), ?, ?, 1)",
            vec![
                rbs::value!(ADMIN_SLUG),
                rbs::value!(ADMIN_FIRST_NAME),
                rbs::value!(ADMIN_LAST_NAME),
                rbs::value!(ADMIN_EMAIL),
                rbs::value!(password_hash),
                rbs::value!(now),
                rbs::value!(ADMIN_STATUS),
                rbs::value!(ADMIN_IMAGE_URL),
            ],
        )
        .await?;

        let author_id: u64 = rb
            .query_decode(
                "select id from author where slug=? limit 1",
                vec![rbs::value!(ADMIN_SLUG)],
            )
            .await?;

        for (slug, title, summary, content, plain, image_url) in PLACEHOLDER_POSTS {
            rb.query(
                "insert into post (author_id, title, slug, summary, publish_type, created_at, content, plain_text_content, image_url, lang) \
                 values (?, ?, ?, ?, 1, to_timestamp(?), ?, ?, ?, 'ru')",
                vec![
                    rbs::value!(author_id),
                    rbs::value!(*title),
                    rbs::value!(*slug),
                    rbs::value!(*summary),
                    rbs::value!(now),
                    rbs::value!(*content),
                    rbs::value!(*plain),
                    rbs::value!(*image_url),
                ],
            )
            .await?;
        }
    }

    rb.query(
        "insert into migration (key, created_at) values (?, to_timestamp(?))",
        vec![rbs::value!(KEY), rbs::value!(time_utils::now_as_secs())],
    )
    .await?;

    Ok(())
}
